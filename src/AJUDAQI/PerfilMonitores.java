package AJUDAQI;

public class PerfilMonitores {
    public Double Avaliacao;
    public String Respotas;
    public String Ajuda;

    public Double getAvaliacao() {
        return Avaliacao;
    }

    public void setAvaliacao(Double avaliacao) {
        Avaliacao = avaliacao;
    }

    public String getRespotas() {
        return Respotas;
    }

    public void setRespotas(String respotas) {
        Respotas = respotas;
    }

    public String getAjuda() {
        return Ajuda;
    }

    public void setAjuda(String ajuda) {
        Ajuda = ajuda;
    }
}
