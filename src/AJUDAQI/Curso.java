package AJUDAQI;

public class Curso {
    private String NomeCurso;
    private Integer Periodo;

    public String getNomeCurso() {
        return NomeCurso;
    }

    public void setNomeCurso(String nomeCurso) {
        NomeCurso = nomeCurso;
    }

    public Integer getPeriodo() {
        return Periodo;
    }

    public void setPeriodo(Integer periodo) {
        Periodo = periodo;
    }
}
